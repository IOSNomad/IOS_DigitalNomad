import UIKit

extension UIColor {
    static var salmon: UIColor {
        return UIColor(red: 254.0 / 255.0, green: 127.0 / 255.0, blue: 102.0 / 255.0, alpha: 1.0)
    }
    
    static var aquamarine: UIColor {
        return UIColor(red: 0.0, green: 204.0 / 255.0, blue: 203.0 / 255.0, alpha: 1.0)
    }
    
    static var greyish: UIColor {
        return UIColor(white: 179.0 / 255.0, alpha: 1.0)
    }
    
    static var darkPeach: UIColor {
        return UIColor(red: 230.0 / 255.0, green: 116.0 / 255.0, blue: 92.0 / 255.0, alpha: 1.0)
    }
    
    static var white: UIColor {
        return UIColor(white: 247.0 / 255.0, alpha: 1.0)
    }
}
